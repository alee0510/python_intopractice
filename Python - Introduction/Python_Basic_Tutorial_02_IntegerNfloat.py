# python tutorial 02 : Integer and Float
print("Tutorial 02 : Integer and Float")

# difine an integer variable
i = 20              # integer
j = 6.0             # float

# basic mathematical operation
print("\nBasic Mathematical Operation")
print(i+j)
print("i+j")        # its a print string variable command
print(i-j)
print(i*j)
print(i/j)
print(i%j)          # modulus
print(i**j)         # ** mean power, or ^ in MATLAB
print(i//j)         # devide

# print methods
print("\nPrint Methods")
print("i is ", i)
print("j is %s" %j) # %s will replace by %j
print("i is {}".format(i))